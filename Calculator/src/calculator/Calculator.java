    private void equalBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_equalBtnMouseClicked
        // TODO add your handling code here:
        String ans;
        
        secondNum = Double.parseDouble(input.getText());
        switch(operation)
        {
            case "+":
                result = firstNum + secondNum;
                ans = String.valueOf(result);
                resutlsDisplay.setText(null);
                input.setText(ans);
                break;
            
            case "-":
                result = firstNum - secondNum;
                ans = String.valueOf(result);
                resutlsDisplay.setText(null);
                input.setText(ans);
                break;
            
            case "x":
                result = firstNum * secondNum;
                ans = String.valueOf(result);
                resutlsDisplay.setText(null);
                input.setText(ans);
                break;
                
            case "/":
                if(secondNum == 0)
                {
                    resutlsDisplay.setText(null);
                    input.setText("ERROR!!");
                }
                
                else
                {
                    result = firstNum / secondNum;
                    ans = String.valueOf(result);
                    resutlsDisplay.setText(null);
                    input.setText(ans);
                }
                break;
                
            case "%":
                result = (firstNum / 100) * secondNum;
                ans = String.valueOf(result);
                resutlsDisplay.setText(null);
                input.setText(ans);
                break;
                    
        }
        
        
    }//GEN-LAST:event_equalBtnMouseClicked

