    private void backspaceBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_backspaceBtnMouseClicked
        // TODO add your handling code here:
        String backspace = null;
        
        if(input.getText().length()> 0 & resutlsDisplay.getText().length() > 0)
        {
            StringBuilder strB_I = new StringBuilder(input.getText());
            strB_I.deleteCharAt(input.getText().length() - 1);
            backspace = strB_I.toString();
            input.setText(backspace);
            
            StringBuilder strB_R = new StringBuilder(resutlsDisplay.getText());
            strB_R.deleteCharAt(resutlsDisplay.getText().length() - 1);
            backspace = strB_R.toString();
            resutlsDisplay.setText(backspace);
        }
    }//GEN-LAST:event_backspaceBtnMouseClicked

